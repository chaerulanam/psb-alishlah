            <h2> <strong>G. Program Kerja</strong> </h2>

            <h3> <strong> I. Program Kerja Jangka Pendek </strong> </h3>

            <ol>
                1. Menyelenggarakan pendidikan dalam rangka menyiapkan sumber daya manusia unggul melalui penataran, pelatihan, kursus dan lain-lain.
                2. Meningkatkan kuota penerimaan santri baru baik lulusan Sekolah Dasar maupun Sekolah Menengah Pertama.
                3. Meningkatkan kemampuan akademik santri dengan pembelajaran remedial (pengayaan) dan belajar tuntas.
                4. Meningkatkan hasil ujian sekolah (US) dan ujian nasional (UN).
            </ol>

            <h3> <strong> II. Program Kerja Jangka Menengah </strong> </h3>
            <ol>
                1. Memfasilitasi tenaga pengajar untuk melanjutkan pendidikan ke jenjang yang lebih tinggi.
                2. Menyediakan sarana dan prasarana pendidik sesuai dengan kebutuhan.
                3. Mengadakan pelatihan bagi para guru yunior dalam rangka meningkatkan kompetensinya dalam mengajar.
                4. Mengadakan pelatihan di bidang administrasi.
            </ol>

            <h3> <strong> III.Program Kerja Jangka Panjang </strong></h3>
            <ol>
                1. Menyediakan fasilitas pendidik yang lebih lengkap.
                2. Merintis pesantren cabang sesuai dengan kebutuhan.
                3. Menyiapkan kader-kader pesantren dengan cara memberikan bea studi kepada lulusan pesantren untuk melanjutkan ke jenjang yang lebih tinggi.
            </ol>

